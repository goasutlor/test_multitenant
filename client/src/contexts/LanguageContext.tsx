export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  // Initialize language from localStorage or default to 'th'
  const [language, setLanguage] = useState<'th' | 'en'>(() => {
    const savedLanguage = localStorage.getItem('language') as 'th' | 'en';
    return savedLanguage || 'th';
  });

  const toggleLanguage = () => {
    const newLanguage = language === 'th' ? 'en' : 'th';
    setLanguage(newLanguage);
    localStorage.setItem('language', newLanguage);
    console.log('üîç Language toggled to:', newLanguage);
  };

  const isThai = language === 'th';

  const t = (key: string, params?: Record<string, any>): string => {
    let translation = translations[language][key] || key;
    
    // Replace parameters in translation
    if (params) {
      Object.keys(params).forEach(param => {
        translation = translation.replace(`{${param}}`, params[param]);
      });
    }
    
    return translation;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, toggleLanguage, isThai, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
